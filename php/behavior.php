<h2>Animal Behavior</h2>

<p>
  Animal behavior is a fascinating part of nature because every animal responds differently to the world around it.
  Their actions are often shaped by weather, danger, food availability, and the need to survive in changing environments.
</p>

<p>
  Some animals depend on teamwork, such as wolves that move in packs and protect one another while hunting.
  Others rely on instinct, like sea turtles that return to the same nesting areas or birds that travel long distances during migration seasons.
  Even small insects like bees and ants show highly organized behavior that helps their colonies function efficiently.
</p>

<p>
  Behavior also helps animals communicate. Dolphins use sound signals, monkeys warn each other about predators,
  and many animals use body movement, scent, or voice to express fear, attraction, or territory.
</p>

